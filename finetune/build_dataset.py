#!/usr/bin/env python3
"""Build the Krish fine-tune dataset straight from the source texts.

This teaches Krish's *voice* — the sarcastic gen-z Krishna tone — grounded in
the actual teachings of the Bhagavad Gita and the moments of the Mahabharata.
It does NOT try to teach facts: factual recall stays the job of RAG at runtime.

Every example is generated by a *local* Ollama model (the "generator"), so the
dataset's quality is capped by that model's quality — pull a bigger GEN_MODEL
for better data (see finetune/README.md).

For each Gita verse we generate ~GITA_PAIRS_PER_VERSE situations and a Krish
reply that lives out that verse's teaching (without quoting it). We then add
~MAHA_PAIRS examples leaning on well-known Mahabharata moments. Each line of the
output jsonl is {"instruction": <student situation>, "output": <Krish reply>}.

Run:  python finetune/build_dataset.py
"""

import json
import os
import random
import re
import sys
from pathlib import Path

ROOT_DIR = Path(__file__).resolve().parent.parent
sys.path.insert(0, str(ROOT_DIR))

from ollama import Client  # noqa: E402
from backend.config import OLLAMA_HOST  # noqa: E402
from backend.persona import KRISHNA_SYSTEM  # noqa: E402

# --- config ---------------------------------------------------------------

GEN_MODEL = os.getenv("GEN_MODEL", "qwen2.5:3b")
# 14 verses x 10 would only be ~140; we scale per-verse up so the total lands
# in the ~250-350 target. Override either knob via env to taste.
GITA_PAIRS_PER_VERSE = int(os.getenv("GITA_PAIRS_PER_VERSE", "18"))
MAHA_PAIRS = int(os.getenv("MAHA_PAIRS", "50"))

GITA_PATH = ROOT_DIR / "data" / "gita.json"
OUT_PATH = ROOT_DIR / "finetune" / "krish_dataset.jsonl"

GEN_OPTIONS = {"temperature": 0.9, "top_p": 0.95}

_client = Client(host=OLLAMA_HOST)

# Realistic student struggles to anchor each example in. (label, description)
SITUATIONS = [
    ("procrastination", "they've been putting off studying for weeks and keep finding excuses"),
    ("placement anxiety", "campus placements are coming and they're terrified of the interviews"),
    ("exam pressure", "finals are in a few days and they're panicking about the results"),
    ("burnout", "they studied flat out and now they're drained with zero motivation left"),
    ("heartbreak", "they just got dumped and can't focus on anything"),
    ("overconfidence", "they think they already know it all and keep skipping the fundamentals"),
    ("comparison", "they're doomscrolling linkedin and everyone seems way ahead of them"),
    ("imposter syndrome", "they feel like a fraud who's about to be exposed any day"),
    ("can't start", "a project is due and they're frozen, staring at a blank screen"),
    ("decision paralysis", "they can't decide which specialization or job offer to pick"),
    ("setback", "they just failed an exam / got rejected and want to quit"),
    ("doomscrolling", "they can't stop scrolling their phone instead of working"),
    ("perfectionism", "they keep polishing and never actually finish or submit anything"),
    ("scared to ask", "they're too embarrassed to ask their doubts and are falling behind"),
    ("fear of failure", "they're scared to even try in case they fail"),
    ("distraction", "friends want to hang out but they have a deadline tomorrow"),
]

# Well-known Mahabharata moments + Krishna's role. (label, description)
MAHA_MOMENTS = [
    ("arjuna's collapse", "arjuna freezing on the battlefield, not wanting to fight his own people"),
    ("the sarathi", "krishna holding the reins as charioteer — he guides, but arjuna must fight his own war"),
    ("the dice game", "the rigged game of dice where one bad call spirals into losing everything"),
    ("karna", "karna's huge talent wasted by loyalty to the wrong side and old grudges"),
    ("the chakravyuha", "abhimanyu entering the chakravyuha knowing how to get in but not out — brave but half-prepared"),
    ("eklavya", "eklavya teaching himself archery in the forest and out-skilling the privileged"),
    ("bhishma's vow", "bhishma trapped for a lifetime by his own rigid terrible promise"),
    ("the unarmed driver", "krishna choosing to drive the chariot unarmed over sending his whole army — leverage over brute force"),
    ("dharma vs adharma", "doing the hard unglamorous right thing when the easy wrong thing is tempting"),
    ("night before war", "the long night of dread before the war finally begins"),
    ("draupadi", "draupadi keeping her composure and fighting back when the system humiliated her"),
    ("ghatotkacha", "ghatotkacha sacrificed to soak up the unstoppable weapon — spending what you have for the bigger goal"),
    ("the peace mission", "krishna exhausting every honest option for peace before the fight"),
    ("ego clouds genius", "brilliant minds like drona and duryodhana undone by attachment and ego"),
]

# --- generation + parsing -------------------------------------------------

_PROMPT = """a student close to you is struggling. what's going on with them: {situation}.

this student is a regular present-day ai/ml student — NOT arjuna. never call \
them arjuna; address them like a normal friend (bro, bhai, by no name at all).

let this wisdom quietly guide your reply — DON'T quote it, DON'T name any chapter \
or verse, just live it:
{grounding}

write ONE training example in EXACTLY this format, nothing before or after it:
SITUATION: <a realistic, specific, first-person message from the student in their \
own casual words, 1-2 sentences>
KRISH: <your reply — short, sarcastic gen-z krishna energy, tough love from a real \
friend, grounded in that wisdom, no lecturing, no verse numbers, 2-4 sentences>"""

_PAIR_RE = re.compile(r"situation\s*:\s*(.*?)\s*krish\s*:\s*(.*)", re.I | re.S)
_VERSE_CITE = re.compile(r"\b\d{1,2}\s*[.:]\s*\d{1,2}\b")


def _clean(s: str) -> str:
    s = s.strip().strip("“”\"'*`").strip()
    s = re.sub(r"\s+", " ", s)
    return s.strip()


def _parse(text: str) -> tuple[str, str] | None:
    m = _PAIR_RE.search(text)
    if not m:
        return None
    instr, out = m.group(1), m.group(2)
    # if the model rambled into another labelled block, keep only the first reply
    out = re.split(r"\bsituation\s*:", out, flags=re.I)[0]
    return _clean(instr), _clean(out)


def _valid(instr: str, out: str) -> bool:
    if not instr or not out:
        return False
    if not (10 <= len(instr) <= 400):
        return False
    if not (15 <= len(out) <= 800):
        return False
    if _VERSE_CITE.search(out):            # no verse numbers in the voice data
        return False
    low = out.lower()
    if "situation:" in low or "krish:" in low:  # leaked format scaffolding
        return False
    return True


def generate(grounding: str, situation: str) -> tuple[str, str] | None:
    prompt = _PROMPT.format(situation=situation, grounding=grounding)
    try:
        resp = _client.chat(
            model=GEN_MODEL,
            messages=[
                {"role": "system", "content": KRISHNA_SYSTEM},
                {"role": "user", "content": prompt},
            ],
            options=GEN_OPTIONS,
        )
    except Exception as e:  # network / model errors — skip this pair
        print(f"  ! generation error: {e}", file=sys.stderr)
        return None
    parsed = _parse(resp["message"]["content"])
    if parsed and _valid(*parsed):
        return parsed
    return None


def _norm(instr: str) -> str:
    """Normalized key for near-identical instruction dedup."""
    return re.sub(r"[^a-z0-9 ]", "", instr.lower()).strip()


def _situation_cycle(n: int) -> list[tuple[str, str]]:
    """A shuffled list of n situations, cycling the pool so each verse gets a
    varied spread rather than the same handful repeated."""
    pool = (SITUATIONS * (n // len(SITUATIONS) + 1))[:n]
    random.shuffle(pool)
    return pool


def main() -> None:
    verses = json.loads(GITA_PATH.read_text(encoding="utf-8"))
    OUT_PATH.parent.mkdir(parents=True, exist_ok=True)

    seen: set[str] = set()
    examples: list[dict] = []
    total = len(verses) * GITA_PAIRS_PER_VERSE + MAHA_PAIRS
    done = 0

    with OUT_PATH.open("w", encoding="utf-8") as f:

        def add(instr: str, out: str) -> None:
            key = _norm(instr)
            if not key or key in seen:
                return
            seen.add(key)
            rec = {"instruction": instr, "output": out}
            f.write(json.dumps(rec, ensure_ascii=False) + "\n")
            f.flush()
            examples.append(rec)

        def progress() -> None:
            if done % 10 == 0 or done == total:
                print(f"  ... {done}/{total} attempts, {len(examples)} kept",
                      file=sys.stderr)

        print(f"generating with GEN_MODEL={GEN_MODEL} "
              f"({len(verses)} verses x {GITA_PAIRS_PER_VERSE} + {MAHA_PAIRS} mahabharata)",
              file=sys.stderr)

        # GITA-GROUNDED
        for v in verses:
            grounding = f"theme: {v['theme']}\nteaching: {v['translation']}"
            for _, desc in _situation_cycle(GITA_PAIRS_PER_VERSE):
                res = generate(grounding, desc)
                done += 1
                if res:
                    add(*res)
                progress()

        # MAHABHARATA-THEME
        for i in range(MAHA_PAIRS):
            _, moment = MAHA_MOMENTS[i % len(MAHA_MOMENTS)]
            _, sdesc = random.choice(SITUATIONS)
            grounding = (
                f"a moment from the mahabharata: {moment}\n"
                "(draw on what you actually know of the epic; same krish voice)"
            )
            res = generate(grounding, sdesc)
            done += 1
            if res:
                add(*res)
            progress()

    # report: 6 random samples + final count
    print(f"\nwrote {len(examples)} examples to {OUT_PATH.relative_to(ROOT_DIR)}\n")
    for rec in random.sample(examples, min(6, len(examples))):
        print("--- sample ---")
        print(f"INSTRUCTION: {rec['instruction']}")
        print(f"OUTPUT:      {rec['output']}\n")
    print(f"TOTAL EXAMPLES: {len(examples)}")


if __name__ == "__main__":
    main()
